package pertemuan3;

public class OperatorAritmatika {
	public static void main (String [] args) {
		/*
		 * Dalam pemrograman ada yang dinamakan operator matematika
		 * merupakan karakter untuk operasi matematis, yaitu :
		 * 1. + untuk penjumlahan
		 * 2. - untuk pengurangan
		 * 3. * untuk perkalian
		 * 4. / untuk pembagian
		 * 5. % untuk modulus (sisa bagi)
		 */
		int a = 5;
		int b = 4;
		System.out.println("Hasil tambah "+ (a+b));
		System.out.println("Hasil pengurangan "+ (a-b));
		System.out.println("Hasil perkalian "+ (a*b));
		System.out.println("Hasil pembagian "+ (a/b));
		System.out.println("Hasil sisa bagi "+ (a%b));
		
	}

}
