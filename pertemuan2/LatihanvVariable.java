package pertemuan2;

public class LatihanvVariable {

	public static void main(String [] args) {
		/*
		 * Variabel adalah wadah penyimpan nilai
		 * Formatnya adalah
		 * [tipedata] [namaVariable] = [nilai];
		 * 
		 * Variabel bersifat replaceable (mudah diganti)
		 * 
		 */
		
		String nama = "Ujang";
		nama = "Herdy";// isi dari nama tertimpa dengan herdy
		System.out.println("Nama saya " + nama);
		
		String nama2 = "Ujang";
		System.out.println("Nama saya " + nama2);
		
		
	}
	
}
